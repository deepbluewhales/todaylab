  async function handleAuthSubmit() {
    if (!supabase) return;
    setAuthError("");
    setAuthMsg("");
    if (!authEmail || !authPassword) {
      setAuthError("이메일과 비밀번호를 입력해주세요.");
      return;
    }
    if (authMode === "signup" && (!authName || !authPhone)) {
      setAuthError("이름과 연락처도 입력해주세요.");
      return;
    }
    setAuthSubmitting(true);
    try {
      if (authMode === "signup") {
        const { data, error } = await supabase.auth.signUp({
          email: authEmail,
          password: authPassword,
          options: {
            data: { name: authName, phone: authPhone },
          },
        });
        if (error) {
          setAuthError(error.message);
        } else if (!data.session) {
          setAuthMsg(
            "가입 확인 메일을 보냈어요. Supabase 설정에서 이메일 인증을 껐다면 이 메시지는 뜨지 않아야 해요 — 대시보드 설정을 확인해주세요."
          );
          setAuthMode("signin");
        }
        // 세션이 바로 생기면(이메일 인증 꺼짐) 프로필 생성은 아래 useEffect에서 처리해요
      } else {
        const { error } = await supabase.auth.signInWithPassword({
          email: authEmail,
          password: authPassword,
        });
        if (error) setAuthError(error.message);
      }
    } catch (e) {
      setAuthError("문제가 발생했어요. 잠시 후 다시 시도해주세요.");
    } finally {
      setAuthSubmitting(false);
    }
  }

